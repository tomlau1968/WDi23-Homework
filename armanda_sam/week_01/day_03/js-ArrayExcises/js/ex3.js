/*
Write a function that takes a character (i.e. a string of length 1)
and returns true if it is a vowel, false otherwise.
*/

const character = function ( str ) {
 if ( str = "a", "e", "i", "o", "u" ) {
   console.log( "vowel" );
 }
 else {
   return "false";
 }
};

 character("spf");
 character("apple");
